drop TABLE IF EXISTS ACTION;
create TABLE ACTION (
	SYMBOL VARCHAR(16777216),
	DATE DATE,
	DIVIDENDS NUMBER(8,4),
	SPLITS NUMBER(10,2)
);
.mode csv
.import --csv --skip 1 action.csv action




